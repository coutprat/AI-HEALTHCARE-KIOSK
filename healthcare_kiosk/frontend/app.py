def patient_registration():
    """Patient Registration Page with improved validation"""
    st.header("üÜï Patient Registration")
    
    # Initialize session state
    if 'registration_complete' not in st.session_state:
        st.session_state.registration_complete = False
    if 'patient_data' not in st.session_state:
        st.session_state.patient_data = None
    
    if not st.session_state.registration_complete:
        with st.form("registration_form"):
            col1, col2 = st.columns(2)
            
            with col1:
                aadhaar = st.text_input(
                    "Aadhaar Number", 
                    max_chars=12,
                    help="Enter exactly 12 digits"
                )
                name = st.text_input(
                    "Full Name",
                    help="Enter your complete name"
                )
            
            with col2:
                age = st.number_input(
                    "Age", 
                    min_value=1, 
                    max_value=120, 
                    value=25,
                    help="Age between 1 and 120"
                )
                gender = st.selectbox(
                    "Gender", 
                    ["Male", "Female", "Other"],
                    help="Select your gender"
                )
            
            register_btn = st.form_submit_button("Register Patient")
            
            if register_btn:
                # Client-side validation
                errors = []
                
                if not aadhaar:
                    errors.append("Aadhaar number is required")
                elif len(aadhaar) != 12:
                    errors.append("Aadhaar must be exactly 12 digits")
                elif not aadhaar.isdigit():
                    errors.append("Aadhaar must contain only digits")
                
                if not name or not name.strip():
                    errors.append("Name is required")
                
                if age < 1 or age > 120:
                    errors.append("Age must be between 1 and 120")
                
                if errors:
                    for error in errors:
                        st.error(f"‚ùå {error}")
                else:
                    # Show loading spinner
                    with st.spinner("Registering patient..."):
                        response = api_client.register_patient(aadhaar, name, age, gender)
                    
                    if "error" not in response:
                        st.success(f"‚úÖ Patient registered successfully! ID: {response['patient_id']}")
                        st.session_state.patient_data = {
                            'name': name,
                            'patient_id': response['patient_id']
                        }
                        st.session_state.registration_complete = True
                        st.rerun()
                    else:
                        st.error(f"‚ùå Registration failed: {response['error']}")
                        if 'status_code' in response:
                            st.error(f"Status Code: {response['status_code']}")
    
    # Step 2: Face Registration (Outside the form)
    else:
        st.success(f"‚úÖ Patient {st.session_state.patient_data['name']} registered successfully!")
        
        st.info("üì∏ Now let's register your face for future check-ins...")
        
        col1, col2 = st.columns(2)
        with col1:
            if st.button("üé• Start Face Registration", type="primary"):
                with st.spinner("Starting face registration..."):
                    success = register_face(st.session_state.patient_data['name'])
                    if success:
                        st.success("‚úÖ Face registration completed!")
                        st.balloons()
                    else:
                        st.error("‚ùå Face registration failed")
        
        with col2:
            if st.button("‚è≠Ô∏è Skip Face Registration"):
                st.info("Face registration skipped. You can register your face later.")
                st.session_state.registration_complete = False
                st.session_state.patient_data = None
                st.rerun()

def patient_checkin():
    """Patient Check-in Page with Fixed Automatic Face Recognition"""
    st.header("üîê Patient Check-in")
    
    tab1, tab2 = st.tabs(["Automatic Face Recognition", "Manual Aadhaar Login"])
    
    with tab1:
        st.subheader("üé• Automatic Face Recognition Check-in")
        st.info("Look at the camera for automatic recognition")
        
        # Show current login status
        if st.session_state.patient_id:
            st.success(f"‚úÖ Already logged in as: {st.session_state.patient_name}")
            if st.button("Continue to Symptom Checker"):
                st.session_state.current_page = "Symptom Checker"
                st.rerun()
            return
        
        col1, col2 = st.columns(2)
        
        with col1:
            if st.button("üöÄ Start Automatic Recognition", type="primary", use_container_width=True):
                with st.spinner("üîç Looking for registered faces... Please look at the camera"):
                    try:
                        # Import the automatic recognition function
                        from face_checkin import automatic_face_checkin
                        
                        # Start automatic recognition
                        recognized_user = automatic_face_checkin(timeout=20)
                        
                        if recognized_user:
                            st.success(f"‚úÖ Face recognized: {recognized_user}")
                            
                            # Get patient details from backend using the recognized name
                            with st.spinner("Fetching patient details..."):
                                response = api_client.login_by_name(recognized_user)
                            
                            if "error" not in response:
                                # Successfully logged in
                                st.session_state.patient_id = response['patient_id']
                                st.session_state.patient_name = response['name']
                                
                                st.success(f"üéâ Welcome back, {response['name']}!")
                                st.balloons()
                                
                                # Auto-redirect to symptom checker
                                import time
                                time.sleep(2)
                                st.session_state.current_page = "Symptom Checker"
                                st.rerun()
                            else:
                                st.error(f"‚ùå Login failed: {response.get('error', 'Patient not found in database')}")
                                st.info("üí° The face was recognized but no matching patient record was found. Please register first.")
                        else:
                            st.error("‚ùå Face not recognized. Please try again or use manual login.")
                            
                    except ImportError:
                        st.error("‚ùå Face recognition module not available. Please use manual login.")
                    except Exception as e:
                        st.error(f"‚ùå Recognition failed: {str(e)}")
        
        with col2:
            st.info("**Instructions:**")
            st.write("‚Ä¢ Position yourself in front of the camera")
            st.write("‚Ä¢ Look directly at the camera")
            st.write("‚Ä¢ Keep your face well-lit")
            st.write("‚Ä¢ Wait for automatic recognition")
            st.write("‚Ä¢ Press 'q' to cancel if needed")
    
    with tab2:
        st.subheader("üìù Manual Aadhaar Login")
        
        with st.form("login_form"):
            aadhaar = st.text_input("Enter Aadhaar Number", max_chars=12)
            login_btn = st.form_submit_button("Login")
            
            if login_btn and len(aadhaar) == 12:
                with st.spinner("Logging in..."):
                    response = api_client.login_patient(aadhaar)
                
                if "error" not in response:
                    st.success(f"‚úÖ Welcome, {response['name']}!")
                    st.session_state.patient_id = response['patient_id']
                    st.session_state.patient_name = response['name']
                    st.session_state.current_page = "Symptom Checker"
                    st.rerun()
                else:
                    st.error("‚ùå Patient not found. Please register first.")

def symptom_checker():
    """AI Symptom Checker Page"""
    if not st.session_state.patient_id:
        st.error("‚ùå Please login first")
        return
    
    st.header("ü©∫ AI Symptom Checker")
    st.subheader(f"Patient: {st.session_state.patient_name}")
    
    tab1, tab2 = st.tabs(["Text Input", "Voice Input"])
    
    with tab1:
        st.subheader("Describe Your Symptoms")
        
        symptoms_text = st.text_area(
            "Enter your symptoms:",
            placeholder="e.g., I have fever, headache, and body pain",
            height=100
        )
        
        if st.button("Get Diagnosis", key="text_diagnosis"):
            if symptoms_text:
                with st.spinner("ü§ñ AI is analyzing your symptoms..."):
                    response = api_client.diagnose_symptoms(
                        st.session_state.patient_id, 
                        symptoms_text
                    )
                    
                    if "error" not in response:
                        st.success("‚úÖ Diagnosis Complete!")
                        
                        col1, col2 = st.columns(2)
                        
                        with col1:
                            st.subheader("üîç Extracted Symptoms")
                            for symptom in response['extracted_symptoms']:
                                st.write(f"‚Ä¢ {symptom}")
                        
                        with col2:
                            st.subheader("üè• Possible Condition")
                            st.write(f"**{response['diagnosis']}**")
                            
                            st.info("‚ö†Ô∏è This is an AI prediction. Please consult a doctor for proper diagnosis.")
                    else:
                        st.error(f"‚ùå Diagnosis failed: {response.get('error', 'Unknown error')}")
            else:
                st.error("‚ùå Please enter your symptoms")
    
    with tab2:
        st.subheader("Voice Input")
        
        if st.button("üé§ Start Voice Recording"):
            try:
                r = sr.Recognizer()
                with sr.Microphone() as source:
                    st.info("üé§ Listening... Please speak your symptoms")
                    audio = r.listen(source, timeout=10)
                    
                    st.info("üîÑ Processing speech...")
                    symptoms_text = r.recognize_google(audio)
                    
                    st.success(f"‚úÖ Heard: {symptoms_text}")
                    
                    # Process with AI
                    response = api_client.diagnose_symptoms(
                        st.session_state.patient_id, 
                        symptoms_text
                    )
                    
                    if "error" not in response:
                        st.subheader("üè• AI Diagnosis")
                        st.write(f"**Condition:** {response['diagnosis']}")
                        st.write(f"**Symptoms:** {', '.join(response['extracted_symptoms'])}")
                    
            except sr.UnknownValueError:
                st.error("‚ùå Could not understand audio")
            except sr.RequestError as e:
                st.error(f"‚ùå Speech recognition error: {e}")
            except Exception as e:
                st.error(f"‚ùå Error: {e}")

def vitals_monitoring():
    """Vitals Monitoring Page"""
    if not st.session_state.patient_id:
        st.error("‚ùå Please login first")
        return
    
    st.header("üíì Vitals Monitoring")
    st.subheader(f"Patient: {st.session_state.patient_name}")
    
